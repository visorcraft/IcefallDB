#!/usr/bin/env python3
"""Generate a Markdown baseline report from DataFusion vs DuckDB JSON results."""

from __future__ import annotations

import argparse
import json
from datetime import datetime, timezone
from pathlib import Path

REPO = Path(__file__).resolve().parents[3]
ROOT = REPO / "target" / "tmp"


def load_matrix(scale: str, engine: str) -> dict:
    path = ROOT / f"icefalldb_query_bench_{scale}_{engine}.json"
    return json.loads(path.read_text())


def _matrix_path(scale: str, engine: str) -> Path:
    return ROOT / f"icefalldb_query_bench_{scale}_{engine}.json"


def _resolve_timestamp(path: Path, override: str | None) -> str:
    if override:
        return override
    if path.exists():
        mtime = path.stat().st_mtime
        return datetime.fromtimestamp(mtime, tz=timezone.utc).isoformat()
    return datetime.now(timezone.utc).isoformat()


def generate(
    scale: str,
    date: str,
    branch: str,
    iters: int,
    clear_cache_per_iter: bool,
    config_notes: str,
    timestamp: str | None,
) -> str:
    datafusion = load_matrix(scale, "datafusion")
    duckdb = load_matrix(scale, "duckdb")

    df_path = _matrix_path(scale, "datafusion")
    iters_val = datafusion.get("iters", iters)
    clear_val = datafusion.get("clear_cache_per_iter", clear_cache_per_iter)
    ts_val = datafusion.get("timestamp") or _resolve_timestamp(df_path, timestamp)

    rows: list[str] = []
    rows.append("| Query | DuckDB (ms) | DataFusion (ms) | DataFusion Δ |\n")
    rows.append("|---|---:|---:|---:|\n")

    df_by_name = {q["query_name"]: q for q in datafusion["queries"]}
    for duck_q in duckdb["queries"]:
        name = duck_q["query_name"]
        df_q = df_by_name[name]
        duck_median = duck_q["median_ms"]
        df_median = df_q["median_ms"]
        delta_pct = (df_median - duck_median) / duck_median * 100.0
        rows.append(
            f"| `{name}` | {duck_median:.3f} | {df_median:.3f} | {delta_pct:+.1f} % |\n"
        )

    cache_note = (
        "cleared before each timed iteration"
        if clear_val
        else "not cleared between iterations"
    )

    return f"""# DataFusion vs. DuckDB — {scale} Benchmark Baseline

Date: {date}
Timestamp: {ts_val}
Branch: `{branch}`
Dataset: `target/tmp/datafusion_bench_db`, scale `{scale}`
Hardware: local development machine

## Reproducibility metadata

- Iterations per query: `{iters_val}`
- Query-result cache: {cache_note} (`--clear-cache-per-iter={"yes" if clear_val else "no"}`)
- Engine/config notes: `{config_notes}`

## Methodology

- {iters_val} iteration(s) per query, single connection per engine.
- DataFusion query-result cache {cache_note} (`--clear-cache-per-iter`).
- DuckDB runs through the same IcefallDB Python adapter against identical Parquet files.
- Optimized variants (`events_sorted`, `events_indexed`, `events_wide_clustered`) were generated by the benchmark generator.

## Results

{"".join(rows)}
## Raw logs

- DuckDB: `target/tmp/icefalldb_query_bench_{scale}_duckdb.json`
- DataFusion: `target/tmp/icefalldb_query_bench_{scale}_datafusion.json`
"""


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate a Markdown baseline report from JSON benchmark results."
    )
    parser.add_argument("--scale", choices=["1m", "10m"], default="10m")
    parser.add_argument("--date", default="2026-06-22")
    parser.add_argument("--branch", default="main")
    parser.add_argument(
        "--iters",
        type=int,
        default=5,
        help="Iterations per query (fallback if not in JSON)",
    )
    parser.add_argument(
        "--clear-cache-per-iter",
        action="store_true",
        help="Whether the cache was cleared per iteration (fallback if not in JSON)",
    )
    parser.add_argument(
        "--config-notes",
        default="target_partitions=16, batch_size=8192",
        help="Engine/configuration notes to include in the baseline doc",
    )
    parser.add_argument(
        "--timestamp",
        default=None,
        help="Override timestamp (defaults to JSON value or file modification time)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=REPO / "docs" / "superpowers" / "baselines",
    )
    args = parser.parse_args()

    doc = generate(
        args.scale,
        args.date,
        args.branch,
        args.iters,
        args.clear_cache_per_iter,
        args.config_notes,
        args.timestamp,
    )
    args.output.mkdir(parents=True, exist_ok=True)
    output_path = args.output / f"{args.date}-datafusion-{args.scale}-baseline.md"
    output_path.write_text(doc)
    print(f"Wrote baseline doc to {output_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
